package pack1;
public class Student
{
    private int rollNo;
    private String name;
    public void setRollNo(int r)
    {
        rollNo = r;
    }
    public void setName(String n)
    {
        name = n;
    }
    public int getRollNo()
    {
        return rollNo;
    }
    public String getName()
    {
        return name;
    }
}
